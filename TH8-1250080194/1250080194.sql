--sp_ThemNhanVien
CREATE OR REPLACE PROCEDURE sp_ThemNhanVien(
p_MaNV VARCHAR2,
p_TenNV VARCHAR2,
p_GioiTinh VARCHAR2,
p_DiaChi VARCHAR2,
p_SoDT VARCHAR2,
p_Email VARCHAR2,
p_TenPhong VARCHAR2,
p_Flag NUMBER,
KQ OUT NUMBER)
AS
BEGIN
IF p_GioiTinh<>'Nam' AND p_GioiTinh<>'Nu' THEN
KQ:=1;
RETURN;
END IF;
IF p_Flag=0 THEN
INSERT INTO NhanVien
VALUES(p_MaNV,p_TenNV,p_GioiTinh,p_DiaChi,p_SoDT,p_Email,p_TenPhong);
ELSE
UPDATE NhanVien
SET TenNV=p_TenNV
WHERE MaNV=p_MaNV;
END IF;
KQ:=0;
END;
--sp_ThemMoiSP
CREATE OR REPLACE PROCEDURE sp_ThemMoiSP(
p_MaSP VARCHAR2,
p_MaHangSX VARCHAR2,
p_TenSP VARCHAR2,
p_SoLuong NUMBER,
p_MauSac VARCHAR2,
p_GiaBan NUMBER,
p_DonViTinh VARCHAR2,
p_MoTa VARCHAR2,
p_Flag NUMBER,
KQ OUT NUMBER)
AS
v_ma VARCHAR2(10);
BEGIN
SELECT MaHangSX INTO v_ma
FROM HangSX
WHERE MaHangSX=p_MaHangSX;
IF p_SoLuong<0 THEN
KQ:=2;
RETURN;
END IF;
IF p_Flag=0 THEN
INSERT INTO SanPham
VALUES(p_MaSP,p_MaHangSX,p_TenSP,p_SoLuong,p_MauSac,p_GiaBan,p_DonViTinh,p_MoTa);
ELSE
UPDATE SanPham
SET TenSP=p_TenSP
WHERE MaSP=p_MaSP;
END IF;
KQ:=0;
EXCEPTION
WHEN NO_DATA_FOUND THEN
KQ:=1;
END;
