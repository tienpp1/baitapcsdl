--sp_NhaphangSX
CREATE OR REPLACE PROCEDURE sp_NhapHangSX(
p_MaHangSX VARCHAR2,
p_TenHang VARCHAR2,
p_DiaChi VARCHAR2,
p_SoDT VARCHAR2,
p_Email VARCHAR2)
AS
v_ma VARCHAR2(10);
BEGIN
SELECT MaHangSX INTO v_ma
FROM HangSX
WHERE TenHang=p_TenHang;

EXCEPTION
WHEN NO_DATA_FOUND THEN
INSERT INTO HangSX
VALUES(p_MaHangSX,p_TenHang,p_DiaChi,p_SoDT,p_Email);
END;
--sp_NhapSP
CREATE OR REPLACE PROCEDURE sp_NhapSP(
p_MaSP VARCHAR2,
p_MaHangSX VARCHAR2,
p_TenSP VARCHAR2,
p_SoLuong NUMBER,
p_MauSac VARCHAR2,
p_GiaBan NUMBER,
p_DonViTinh VARCHAR2,
p_MoTa VARCHAR2)
AS
v_ma VARCHAR2(10);
BEGIN
SELECT MaSP INTO v_ma
FROM SanPham
WHERE MaSP=p_MaSP;
UPDATE SanPham
SET TenSP=p_TenSP,
SoLuong=p_SoLuong,
MauSac=p_MauSac,
GiaBan=p_GiaBan,
DonViTinh=p_DonViTinh,
MoTa=p_MoTa
WHERE MaSP=p_MaSP;
EXCEPTION
WHEN NO_DATA_FOUND THEN
INSERT INTO SanPham
VALUES(p_MaSP,p_MaHangSX,p_TenSP,p_SoLuong,p_MauSac,p_GiaBan,p_DonViTinh,p_MoTa);
END;
--sp_XoaHangSX
CREATE OR REPLACE PROCEDURE sp_XoaHangSX(
p_TenHang VARCHAR2)
AS
v_ma VARCHAR2(10);
BEGIN
SELECT MaHangSX INTO v_ma
FROM HangSX
WHERE TenHang=p_TenHang;
DELETE FROM SanPham
WHERE MaHangSX=v_ma;
DELETE FROM HangSX
WHERE MaHangSX=v_ma;
END;
--sp_NhapNhanVien (FLAG)
CREATE OR REPLACE PROCEDURE sp_NhapNhanVien(
p_MaNV VARCHAR2,
p_TenNV VARCHAR2,
p_GioiTinh VARCHAR2,
p_DiaChi VARCHAR2,
p_SoDT VARCHAR2,
p_Email VARCHAR2,
p_TenPhong VARCHAR2,
p_Flag NUMBER)
AS
BEGIN
IF p_Flag=0 THEN
UPDATE NhanVien
SET TenNV=p_TenNV,
GioiTinh=p_GioiTinh,
DiaChi=p_DiaChi,
SoDT=p_SoDT,
Email=p_Email,
TenPhong=p_TenPhong
WHERE MaNV=p_MaNV;
ELSE
INSERT INTO NhanVien
VALUES(p_MaNV,p_TenNV,p_GioiTinh,p_DiaChi,p_SoDT,p_Email,p_TenPhong);
END IF;
END;
--sp_NhapNhap
CREATE OR REPLACE PROCEDURE sp_NhapNhap(
p_SoHDN VARCHAR2,
p_MaSP VARCHAR2,
p_MaNV VARCHAR2,
p_NgayNhap DATE,
p_SoLuong NUMBER,
p_DonGia NUMBER)
