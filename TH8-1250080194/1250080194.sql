END;
--sp_NhapXuat
CREATE OR REPLACE PROCEDURE sp_NhapXuat(
p_SoHDX VARCHAR2,
p_MaSP VARCHAR2,
p_MaNV VARCHAR2,
p_NgayXuat DATE,
p_SoLuong NUMBER)
AS
v_sl NUMBER;
BEGIN
SELECT SoLuong INTO v_sl
FROM SanPham
WHERE MaSP=p_MaSP;
IF p_SoLuong>v_sl THEN
RETURN;
END IF;
INSERT INTO PXuat
VALUES(p_SoHDX,p_NgayXuat,p_MaNV);
INSERT INTO Xuat
VALUES(p_SoHDX,p_MaSP,p_SoLuong);
END;
